    public statisticsExhibitor() throws SQLException {
        initComponents();
        db = new DBHelper();
        db.openConnection();
        ResultSet rs = db.getCallAble("{Call countExhibitor(1)}").executeQuery();
        int S = 0,P = 0, B = 0, C = 0;
        while(rs.next()){
            S = rs.getInt(1);
            P = rs.getInt(2);
            B = rs.getInt(3);
            C = rs.getInt(4);
        }
        rs.close();
        countS.setText(Integer.toString(S));
        countP.setText(Integer.toString(P));
        sumB.setText(Integer.toString(B));
        countC.setText(Integer.toString(C));
    }

